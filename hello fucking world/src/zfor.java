public class zfor {
    public static void main(String[] args) {
       int i=0, k=0;

       for(i=1;i<11;i++)
       {
           for (k = 1; k < 11; k++)
           {
               System.out.print(i+"*"+k+"="+i*k+" ");



           }
           System.out.println("\n");

       }
    }
}